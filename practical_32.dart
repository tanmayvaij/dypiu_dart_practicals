/*
Find the absolute value of a number, round a number to its nearest value, 
find the hash value of a string
*/

void main() {

  double i = -89.72;

  // Printing absolute value
  print(i.abs());

  // Printing floor value
  print(i.floor());

  String str = "Sample String";

  // Getting hashcode
  print(str.hashCode);

}
